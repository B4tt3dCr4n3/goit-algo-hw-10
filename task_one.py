"""Завдання 1. Оптимізація виробництва

Компанія виробляє два види напоїв: "Лимонад" і "Фруктовий сік". Для виробництва цих 
напоїв використовуються різні інгредієнти та обмежена кількість обладнання. Задача 
полягає у максимізації виробництва, враховуючи обмежені ресурси.

Умови завдання:

1. "Лимонад" виготовляється з "Води", "Цукру" та "Лимонного соку".
2. "Фруктовий сік" виготовляється з "Фруктового пюре" та "Води".
3. Обмеження ресурсів: 100 од. "Води", 50 од. "Цукру", 30 од. "Лимонного соку" 
та 40 од. "Фруктового пюре".
4. Виробництво одиниці "Лимонаду" вимагає 2 од. "Води", 1 од. "Цукру" та 1 од. 
"Лимонного соку".
5. Виробництво одиниці "Фруктового соку" вимагає 2 од. "Фруктового пюре" та 1 од. "Води".

Використовуючи PuLP, створіть модель, яка визначає, скільки "Лимонаду" та "Фруктового 
соку" потрібно виробити для максимізації загальної кількості продуктів, дотримуючись 
обмежень на ресурси. Напишіть програму, код якої максимізує загальну кількість вироблених 
продуктів "Лимонад" та "Фруктовий сік", враховуючи обмеження на кількість ресурсів."""

import pulp

# Створюємо задачу лінійного програмування для максимізації
problem = pulp.LpProblem("Максимізація виробництва", pulp.LpMaximize)

# Змінні: кількість виробництва Лимонаду та Фруктового соку
lemonade = pulp.LpVariable('Лимонад', lowBound=0, cat='Continuous')
fruit_juice = pulp.LpVariable('Фруктовий_сік', lowBound=0, cat='Continuous')

# Функція цілі: максимізувати загальну кількість вироблених продуктів
problem += lemonade + fruit_juice, "Загальне_виробництво"

# Обмеження на ресурси
problem += 2 * lemonade + 1 * fruit_juice <= 100, "Обмеження_на_воду"
problem += 1 * lemonade <= 50, "Обмеження_на_цукор"
problem += 1 * lemonade <= 30, "Обмеження_на_лимонний_сік"
problem += 2 * fruit_juice <= 40, "Обмеження_на_фруктове_пюре"

# Розв'язуємо задачу
problem.solve()

# Виводимо результати українською мовою
print(f"Статус: {pulp.LpStatus[problem.status]}")
print(f"Виробництво лимонаду: {pulp.value(lemonade)} одиниць")
print(f"Виробництво фруктового соку: {pulp.value(fruit_juice)} одиниць")
print(f"Загальне виробництво: {pulp.value(problem.objective)} одиниць")
